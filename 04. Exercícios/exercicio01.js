// Crie uma função que dado dois valores (passados como parâmetros) mostre no console a
// soma, subtração, multiplicação e divisão desses valores

function operacoesMatematicas(a, b) {
    console.log(`${a} + ${b} = ${a + b}`);
    console.log(`${a} - ${b} = ${a - b}`);
    console.log(`${a} * ${b} = ${a * b}`);
    console.log(`${a} / ${b} = ${a / b}`);
}

operacoesMatematicas(10, 5);