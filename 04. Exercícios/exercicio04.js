// Crie uma função que irá receber dois valores, o dividendo e o divisor.
// A função deverá imprimir o resultado e o resto da divisão destes dois valores.

function divisao(dividendo, divisor) {
    console.log(`Divisão de ${dividendo} por ${divisor} = ${dividendo / divisor}`);
    console.log(`Resto da divisão de ${dividendo} por ${divisor} = ${dividendo % divisor}`);
}

divisao(2, 2);
divisao(3, 2);